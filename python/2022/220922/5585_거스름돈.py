# 그리디 알고리즘 문제

# 카운터에서 1000엔 지폐를 한장 냈을 때, 받을 잔돈에 포함된 잔돈의 개수
change = 1000-int(input()) # 거스름돈은 얼마인지
yen = [500, 100, 50, 10, 5, 1] # 엔의 잔돈 종류
cnt = 0 # 잔돈의 개수 0으로 초기화
for i in yen:
    cnt += change//i # 거스름돈을 i로 나눈 몫을 cnt에 더한다.
    change %= i # 거스름돈 : 거스름돈을 i로 나눈 나머지를 더하여 갱신 => 999, 499, 99, 49, 9, 4, 0
print(cnt)
